    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String des[] = jTextField2.getText().split(" ");
        b = new BigInteger[des.length];
        for(int i=0;i<des.length;i++){
            b[i] = BigInteger.valueOf(Integer.parseInt(des[i]));
        }
        jTextArea2.setText(this.cipher.descifrar(b));
        
    }
